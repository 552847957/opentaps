package org.opentaps.tests.analytics.tests;

/**
 * Expected result for report that analyze distribution of customers by countries and states.<br>
 * Simple class that represents one row in expected results table. Just set of getters/setters
 * for each field. <br>Naming should conform Java Bean conventions.
 */
public class CustomersByGeoLocation {
    private String country;
    private String state;
    private int count;

    public CustomersByGeoLocation() {
        this.country = null;
        this.state = null;
        this.count = 0;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public int getQuantity() {
        return count;
    }

    public void setQuantity(int count) {
        this.count = count;
    }
    
}
